<?php

namespace App\Nova;

use App\Nova\Actions\MoveToDone;
use App\Nova\Metrics\Orders;
use App\Nova\Metrics\Sales;
use Illuminate\Http\Request;
use Laravel\Nova\Fields\Badge;
use Laravel\Nova\Fields\BelongsTo;
use Laravel\Nova\Fields\Currency;
use Laravel\Nova\Fields\HasMany;

class Order extends Resource
{
    /**
     * The model the resource corresponds to.
     *
     * @var string
     */
    public static $model = \App\Models\Order::class;

    /**
     * The single value that should be used to represent the resource when being displayed.
     *
     * @var string
     */
    public static $title = 'id';

    /**
     * The columns that should be searched.
     *
     * @var array
     */
    public static $search = [
        'id',
    ];

    /**
     * Get the fields displayed by the resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function fields(Request $request)
    {
        return [
            BelongsTo::make('Customer'),
            Badge::make('Status')
                ->map([
                    'PENDING' => 'warning',
                    'DONE' => 'success',
                ]),
            Currency::make('Total Cost', function () {
                $cost = 0;
                $pos = $this->productOrders->load('product');
                foreach ($pos as $po) {
                    $cost += $po->product->price * $po->quantity;
                }
                return $cost;
            }),
            HasMany::make('Product Order', 'productOrders', ProductOrder::class),
        ];
    }

    /**
     * Get the cards available for the request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function cards(Request $request)
    {
        return [
            Orders::make(),
            Sales::make(),
        ];
    }

    /**
     * Get the filters available for the resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function filters(Request $request)
    {
        return [];
    }

    /**
     * Get the lenses available for the resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function lenses(Request $request)
    {
        return [];
    }

    /**
     * Get the actions available for the resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function actions(Request $request)
    {
        return [
            MoveToDone::make()->onlyOnTableRow(),
        ];
    }
}
